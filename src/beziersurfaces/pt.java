package beziersurfaces;

public class pt {
    private double x;
    private double y;
    private double z;
    
    public pt(double x, double y, double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
    
    public double get_X(){
        return x;
    }
    
    public double get_Y(){
        return y;
    }
    
    public double get_Z(){
        return z;
    }
}
